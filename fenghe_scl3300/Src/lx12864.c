void lx12864_gpio_init()
{
       
  GPIO_InitTypeDef  GPIO_InitStruct = {0};
 
  GPIO_InitStruct.Pin	= LX12864_RES_PIN | LX12864_A0_PIN | LX12864_SCK_PIN | LX12864_SID_PIN;       
  GPIO_InitStruct.Mode = GPIO_MODE_OUTPUT_PP;    
  GPIO_InitStruct.Pull = GPIO_NOPULL;    
  GPIO_InitStruct.Speed = GPIO_SPEED_FREQ_HIGH; 
  HAL_GPIO_Init(LX12864_SCK_PORT, &GPIO_InitStruct);
  
  GPIO_InitStruct.Pin	= LX12864_CS_PIN;       
  GPIO_InitStruct.Mode = GPIO_MODE_OUTPUT_PP;    
  GPIO_InitStruct.Pull = GPIO_NOPULL;    
  GPIO_InitStruct.Speed = GPIO_SPEED_FREQ_HIGH; 
  HAL_GPIO_Init(LX12864_CS_PORT, &GPIO_InitStruct);
  

}

//串口模式下只能写不能读,也不能查忙,因此用户要控制好速度不要太快
void WriteCommand( uint8_t CommandByte ) {
	uint8_t i;
	LX12864_CS_LOW;
	LX12864_A0_LOW; //Command
	for(i=0;i<8;i++)
	{ 
		LX12864_SCK_HIGH;
		if((CommandByte << i) & 0x80)  //0x80(1000 0000)  只会保留最高位
		{
		    LX12864_SID_HIGH;           // 引脚输出高电平，代表发送1
		}
		else
		{
			LX12864_SID_LOW;         // 引脚输出低电平，代表发送0
		}
		LX12864_SCK_LOW;
		delay_us(5);
		LX12864_SCK_HIGH;
	} 
	LX12864_CS_HIGH;
}
void WriteData( uint8_t DataByte ) {
	uint8_t i;
	LX12864_CS_LOW;
	LX12864_A0_HIGH;
	for(i=0;i<8;i++){
		LX12864_SCK_HIGH;
		if((DataByte << i) & 0x80)  //0x80(1000 0000)  只会保留最高位
		{
		    LX12864_SID_HIGH;           // 引脚输出高电平，代表发送1
		}
		else
		{
			LX12864_SID_LOW;         // 引脚输出低电平，代表发送0
		}
		LX12864_SCK_LOW;
		delay_us(5);
		LX12864_SCK_HIGH;
	} 
	LX12864_CS_HIGH;
}

void LxClear( uint8_t FillData ) {
	uint8_t i,j;
	for(i=0;i<8;i++)
	{
		WriteCommand(0xB0|ComTable[i]); //Set Page Address
		WriteCommand(0x10); //Set Column Address = 0
		WriteCommand(0x01); //Colum from S1 -> S128 auto add
		for(j=0;j<128;j++)
		{
			WriteData( FillData );
		} 
	} 
}
void Lx_Init( void ) {
	delay_ms(10);
	LX12864_RES_LOW;
	delay_ms(200);
	LX12864_RES_HIGH;
	delay_ms(50);
	WriteCommand(0xAE); //Display OFF
	WriteCommand(0xA2); //1/64 Duty 1/9 Bias
	WriteCommand(0xA0); //ADC select S0->S131(玻璃设计用 S1-S128)
	WriteCommand(0xC0); //com1 --> com64
	WriteCommand(0x24); //对某些模块没用,用的外部 Rb/Ra
	WriteCommand(0x81); //Sets V0
	WriteCommand(48); //内部电位器调节对比度
	WriteCommand(0x2F); //voltage follower ON regulator ON booster ON
	WriteCommand(0xA6); //Normal Display (not reverse dispplay)
	WriteCommand(0xA4); //Entire Display Disable
	WriteCommand(0x40); //Set Display Start Line = com0
	WriteCommand(0xB0); //Set Page Address = 0
	WriteCommand(0x10); //Set Column Address 4 higher bits = 0
	WriteCommand(0x01); //Set Column Address 4 lower bits = 1 , from IC SEG1 -> SEG128
	LxClear(0);
	WriteCommand(0xAF); //Display ON
}
//显示 ASICC 字符的函数
void LxPutChar(uint8_t col,uint8_t page,uint8_t Order)
{
	uint8_t i;
	uint16_t x;
	x = (Order-0x20)*0x10; //ASICC 字符从 0x20 开始,每个 16 byte
	WriteCommand(ComTable[page&0x07]|0xB0); //Set Page Address
	WriteCommand( ((col+1)>>4) | 0x10); //Set Column Address High Byte
	WriteCommand( (col+1)&0x0F ); //Low Byte Colum from S128 -> S1 auto add
	for(i=0;i<8;i++)
	{
		WriteData( ASCIIchardot[x] );
		x++;
	}
	page++; //下半字符 page+1
	WriteCommand(ComTable[page&0x07]|0xB0); //Set Page Address
	WriteCommand( ((col+1)>>4) | 0x10); //Set Column Address High Byte
	WriteCommand( (col+1)&0x0F ); //Low Byte Colum from S128 -> S1 auto add
	for(i=0;i<8;i++)
	{
		WriteData( ASCIIchardot[x] );
		x++;
	}
	page--; //写完一个字符 page 还原
}

void LxPutStr(uint8_t col,uint8_t page,uint8_t *puts)
{
	while(*puts != '\0') //判断字符串时候显示完毕
	{
		if(col>(LxXPixel-8)) //判断行末空间是否足够放一个字符,自动换行
		{
			page=page+2;
			col=0;
		}
		if(page>(LxYPixel/8-2)) //到了屏幕最下角,自动返回左上角
		{
			page=0;
			col=0;
		}
		LxPutChar(col,page,*puts);
		puts++;
		col=col+8; //下一个字符 8 列之后
	} 
}
//显示 3 位数的数值(0-255)
void LxPutNum(uint8_t col,uint8_t page,uint8_t Num)
{
	uint8_t a,b,c;
	a=Num/100;
	b=(Num%100)/10;
	c=Num%10;
	if(a==0) 
		; //也不写空格,直接跳过去//PutChar(col,page,0x20);
	else 
		LxPutChar(col,page,a+0x30);
	if(a==0 && b==0) 
		; //也不写空格,直接跳过去//LxPutChar(col,page,0x20);
	else 
		LxPutChar(col+8,page,b+0x30);
	LxPutChar(col+16,page,c+0x30);
}
void LxPutBmp( uint8_t *puts ) {
	uint8_t i,j;
	uint16_t X=0;
	for(i=0;i<(LxYPixel/8);i++)
	{
		WriteCommand(0xB0|ComTable[i]); //Set Page Address
		WriteCommand(0x10); //Set Column Address = 0
		WriteCommand(0x01); //Colum from S1 -> S128 auto add
		for(j=0;j<LxXPixel;j++)
		{
			WriteData( puts[X] );
			X++;
		} 
	} 
}

